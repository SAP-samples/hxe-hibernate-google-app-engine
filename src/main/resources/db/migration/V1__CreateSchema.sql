CREATE COLUMN TABLE "INCIDENT" ("PD_ID" BIGINT NOT NULL ,
	 "ADDRESS" VARCHAR(255),
	 "CATEGORY" VARCHAR(255),
	 "DATE" TIMESTAMP,
	 "DAY_OF_WEEK" VARCHAR(255),
	 "DESCRIPTION" VARCHAR(255),
	 "INCIDENT_NUMBER" VARCHAR(255),
	 "LOCATION" VARCHAR(255),
	 "PD_DISTRICT" VARCHAR(255),
	 "RESOLUTION" VARCHAR(255),
	 "TIME" VARCHAR(255),
	 "X" DOUBLE NOT NULL ,
	 "Y" DOUBLE NOT NULL ,
	 PRIMARY KEY ("PD_ID"));
	 
CREATE COLUMN TABLE "ADDRESS" ("BASE_ID" BIGINT NOT NULL ,
	 "ADDRESS" VARCHAR(255),
	 "ADDRESS_NUMBER" VARCHAR(255),
	 "ADDRESS_NUMBER_SUFFIX" VARCHAR(255),
	 "CNN" VARCHAR(255),
	 "LOCATION" VARCHAR(255),
	 "STREET_NAME" VARCHAR(255),
	 "STREET_TYPE" VARCHAR(255),
	 "X" DOUBLE NOT NULL ,
	 "Y" DOUBLE NOT NULL ,
	 "ZIP_CODE" VARCHAR(255),
	 PRIMARY KEY ("BASE_ID"));